function score = e022_letter2num(letter)
       if letter == 'A'
            score = 1;
       elseif letter == 'B'
            score = 2;
       elseif letter == 'C'
            score = 3;
       elseif letter == 'D'
            score = 4;
       elseif letter == 'E'
            score = 5;
       elseif letter == 'F'
            score = 6;
       elseif letter == 'G'
            score = 7;
       elseif letter == 'H'
            score = 8;
       elseif letter == 'I'
            score = 9;
       elseif letter == 'J'
            score = 10;
       elseif letter == 'K'
            score = 11;
       elseif letter == 'L'
            score = 12;
       elseif letter == 'M'
            score = 13;
       elseif letter == 'N'
            score = 14;
       elseif letter == 'O'
            score = 15;
       elseif letter == 'P'
            score = 16;
       elseif letter == 'Q'
            score = 17;
       elseif letter == 'R'
            score = 18;
       elseif letter == 'S'
            score = 19;
       elseif letter == 'T'
            score = 20;
       elseif letter == 'U'
            score = 21;
       elseif letter == 'V'
            score = 22;
       elseif letter == 'W'
            score = 23;
       elseif letter == 'X'
            score = 24;
       elseif letter == 'Y'
            score = 25;
       elseif letter == 'Z'
            score = 26;
       elseif letter == 'Å'
            score = 27;
       elseif letter == 'Ä'
            score = 28;
       else
            score = 29;
       end
end
